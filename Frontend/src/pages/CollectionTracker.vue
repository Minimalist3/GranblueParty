    hasAll: true,
    key: "ri",
  },
  {
    name: "Element",
    isColumn: true,
    isFilter: true,
    hasAll: true,
    key: "e",
  },
  {
    name: "Type",
    isColumn: true,
    isFilter: true,
    hasAll: true,
    key: "t",
  },
  {
    name: "Race",
    isColumn: true,
    isFilter: true,
    hasAll: true,
    key: "ra",        
  },
  {
    name: "Weapon",
    isColumn: true,
    isFilter: true,
    hasAll: true,
    key: "w",
  },
  {
    name: "Owned",
    isColumn: false,
    isFilter: true,
    hasAll: false,
    key: "owned",
  },
];

function getDataModel() {
  // Copy the data model locally to modify "checked" properties
  return Object.fromEntries(categories.map(c => [c.key, Utils.copy(DataModel[c.key])]));
}

function getDataCount() {
  return Object.fromEntries(categories.map(c => {
    if (DataModel[c.key].data) {
      return [c.key, DataModel[c.key].data.map(d => 0)];
    }
    return [c.key, []];
  }));
}

const INITIAL_DATA = () => {
  return {
    // Add the corresponding values in the store (collection-tracker.js)
    chara_show: {
      null: true, 10: true, 20: true, 500: true,
      1000: true, 1010: true, 1020: true, 1030: true, 1040: true, 1050: true,
      1500: true, 1600: true,
    },
    summon_show: {
      null: true, 20: true, 500: true,
      1000: true, 1020: true, 1030: true,
      1600: true,
    },
    data_model: getDataModel(),
    // 0: characters, 1: summons
    data_count: [getDataCount(), getDataCount()],
    modification: false,
    showNames: true,
    showStars: true,
    showAwakening: false,
    showCharacters: true,
    showSummons: true,
    showStats: false,
    show_modal_url: false,
    clipboard_text: '',
    loading: true,
  }
};

export default {
  components: {
    Checkbox,
    StatInput,
    DataFilter,
    ModalUrl,
    StarsLine,
  },
  mixins: [
    collectionStoreMixin
  ],
  head: {
    title: 'Granblue.Party - Collection Tracker',
    desc: 'Track the characters and summons you unlocked, and share your collection with your friends',
    image: 'https://www.granblue.party/img/card_collection.jpg',
    keywords: 'collection, tracker, characters, summons, share'
  },
  data() {
    return INITIAL_DATA();
  },
  methods: {
    getName(element) {
      if (this.isLangEnglish) {
        return element.n;
      }
      return element.nj;
    },
    setShowCharacters() {
      this.showCharacters = ! this.showCharacters;
      lsMgt.setValue('showCharacters', this);
    },
    setShowSummons() {
      this.showSummons = ! this.showSummons;
      lsMgt.setValue('showSummons', this);
    },
    selectChara(object) {
      if (this.isOwnCollection) {
        object.owned = ! object.owned;
        if (object.owned) {
          this.chara_count.sum++;
          this.chara_count[object.d]++;
        }
        else {
          this.chara_count.sum--;
          this.chara_count[object.d]--;
        }
        this.modification = true;
      }
    },
    selectSummon(object) {
      if (this.isOwnCollection) {
        object.owned = ! object.owned;
        if (object.owned) {
          this.summon_count.sum++;
          this.summon_count[object.d]++;
        }
        else {
          this.summon_count.sum--;
          this.summon_count[object.d]--;
        }
        this.modification = true;
      }
    },
    starsModified() {
      if (this.isOwnCollection) {
        this.modification = true;
      }
    },
    saveCollection() {
      if (this.isOwnCollection) {
        const postData = {c: [], s: []};
        this.characters.forEach(cat => {
          cat.forEach(chara => {
            if (chara.owned != null) {
              postData.c.push([chara.id, chara.sc, chara.owned, chara.aw]);
            }
          });
        });
        this.summons.forEach(cat => {
          cat.forEach(summ => {
            if (summ.owned != null) {
              postData.s.push([summ.id, summ.sc, summ.owned]);
            }
          });
        });

        this.axios.post('/tracker/save', postData)
          .then(response => this.$store.dispatch('addMessage', {message: 'Collection saved successfully'}))
          .catch(error => this.$store.dispatch('addAxiosErrorMessage', error))
      }
    },
    openInWiki() {
      let strings = {
        'c': ["","",""], // [R, SR, SSR]
        's': ["","",""],
      };
      let selected = {
        'c': [{},{},{}],
        's': [{},{},{}],
      };

      this.characters.flat().forEach(unit => {
        if (unit.owned === true) {
          const unit_id = unit.id.toString();
          const short_id = unit_id.slice(2, 3) + unit_id.slice(4);
          if (short_id.length === 4) {
            const rarity = unit.ri;
            const index = parseInt(short_id.slice(1), 10);
            // Wiki evo count doesn't go higher than 6 for transcendance
            selected.c[rarity][index] = parseInt(Math.min(unit.sc, 6), 10) + 1;
          }
        }
      });
      this.summons.flat().forEach(unit => {
        if (unit.owned === true) {
          const unit_id = unit.id.toString();
          const short_id = unit_id.slice(2, 3) + unit_id.slice(4);
          if (short_id.length === 4) {
            const rarity = unit.ri;
            const index = parseInt(short_id.slice(1), 10);
            // Wiki evo count doesn't go higher than 6 for transcendance
            selected.s[rarity][index] = parseInt(unit.sc, 10) + 1;
          }
        }
      });

      // convert to a bit array
      ['c', 's'].forEach(type => {
        [0, 1, 2].forEach(rarity => {
          let high_id = 0;
          const obj = selected[type][rarity];
          for (let index in obj)
            high_id = Math.max(high_id, index);

          // Group 8 items with 3 bits each
          const parts = Math.floor(high_id / 8) + 1;
          const size = parts * 3;
          let buffer = new Uint8Array(size);

          for (let i = 0; i <= Math.floor(high_id / 8); i++) {
            let evos = 0x000000;
            for (let j = 0; j < 8; j++) {
              let evo = obj[i*8+j];
              if (evo == undefined)
                evo = 0;
              evos |= (evo << (j*3));
            }
            buffer[i*3]   = (evos >>  0) & 0xFF;
            buffer[i*3+1] = (evos >>  8) & 0xFF;
            buffer[i*3+2] = (evos >> 16) & 0xFF;
          }
          const base64 = base64js.fromByteArray(buffer)
          if (base64 != 'AAAA') {
            strings[type][rarity] += base64;
          }
        });
      });

      const wiki_url = "https://gbf.wiki/Collection_Tracker#o3." + strings.c[2] + "." + strings.c[1] + "." + strings.c[0]
        + "." + strings.s[2] + "." + strings.s[1] + "." + strings.s[0];
      window.open(wiki_url);
    },
    shareCollection() {
      const text = window.location.href  + '/' + this.$store.getters.getUserId;
      // Put in clipboard
      this.clipboard_text = text;
      let self = this;
      this.$nextTick().then(() => {
        const input = document.getElementById("clipboardInput");
        input.select();
        document.execCommand("copy");
        self.clipboard_text = '';

        self.$store.dispatch('addMessage', {message: 'URL copied to clipboard'})
      });
    },
    loadCollection() {
      let userId = "";
      if (this.isOwnCollection) {
        userId = this.$store.getters.getUserId;
      }
      else {
        // Defined when viewing other collections
        userId = this.$route.params.collection_id;
      }

      return this.$store.dispatch('collection/fetchCollection', userId);
    },
    loadWikiCollection(url) {
      let units = new Map();

      let url_parts = url.split('#');
      if (url_parts.length == 1) {
        url_parts = url_parts[0];
      }
      else {
        url_parts = url_parts[1];
      }

      let parts = url_parts.split('.');
      for (let i = 1; i <= 7; i++) {
        if (typeof parts[i] !== 'string') {
          parts[i] = '';
        }
      }
      const strings = { 
        3020000: parts[3], 3030000: parts[2], 3040000: parts[1],
        2020000: parts[6], 2030000: parts[5], 2040000: parts[4]
      };

      // Is URL valid ?
      if (parts[1].length + parts[2].length + parts[3].length + parts[4].length + parts[5].length + parts[6].length < 1) {
        this.$store.dispatch('addMessage', {title: 'Error', message: 'Invalid wiki collection URL'});
        return;
      }

      for (let [key, value] of Object.entries(strings)) {
        if(value.length < 1)
          continue;
        
        let buffer = base64js.toByteArray(value);
        let len = buffer.length / 3;
        for (let i = 0; i < len; i++) {
          let evos = 0;
          evos |= (buffer[i*3  ] <<  0);
          evos |= (buffer[i*3+1] <<  8);
          evos |= (buffer[i*3+2] << 16);

          for (let j = 0; j < 8; j++) {
            let evo = ((evos >> (j*3)) & 0x07) - 1;
            if (evo < 0)
              continue;
            let short_id = parseInt(key, 10) + (i*8+j);
            units.set(short_id, evo);
          }
        }
      }

      this.characters.flat().forEach(c => {
        const stars = units.get(c.id);
        if (stars !== undefined) {
          c.owned = true;
          c.sc = stars;
        }
        else if (c.owned === true) {
          c.owned = false;
        }
      });
      this.summons.flat().forEach(c => {
        const stars = units.get(c.id);
        if (stars !== undefined) {
          c.owned = true;
          c.sc = stars;
        }
        else if (c.owned === true) {
          c.owned = false;
        }
      });

      this.modification = true;
    },
  },
  computed: {
    ...mapState('collection', [
      'characters',
      'chara_count',
      'chara_total',
      'summons',
      'summon_count',
      'summon_total'
    ]),
    MAX_AWAKENING() {
      return 9;
    },
    getFilters() {
      return categories.filter(c => { return c.isFilter });
    },
    filtersActive() {
      return Object.values(this.chara_show).some(c => !c) || Object.values(this.summon_show).some(c => !c)
    },
    isOwnCollection() {
      return this.$route.params.collection_id === undefined;
    },
    isLangEnglish() {
      return this.$store.getters.getLang === LANGUAGES.EN;
    },
    getCharacters() {
      if (this.characters) {
        const newDataCount = getDataCount();
        const characters = this.characters.map(element =>
          element.filter(chara => {
            if ( ! this.chara_show.hasOwnProperty(chara.d)) {
              console.log('Unknown Character category ' + chara.d + '. Please report this error to the administrator.');
              return true;
            }
            if ( ! this.chara_show[chara.d]) {
              return false;
            }
            const show = this.getFilters.every(e => {
              return this.data_model[e.key].show(chara[e.key])
            });
            // Increment filter counts
            if (show && this.showCharacters) {
              this.getFilters.forEach(e => {
                this.data_model[e.key].add(chara[e.key], newDataCount[e.key]);
              });
            }
            return show;
          })
        );
        this.$set(this.data_count, 0, newDataCount);
        return characters;
      }
      return [[],[],[],[],[],[],[]];
    },
    getSummons() {
      if (this.summons) {
        const newDataCount = getDataCount();
        const summons = this.summons.map(element =>
          element.filter(summ => {
            const show = this.getFilters.every(e => {
              // Summons have less filters than characters
              if (summ[e.key] === undefined) {
                return true;
              }
              if ( ! this.summon_show.hasOwnProperty(summ.d)) {
                console.log('Unknown Summon category ' + summ.d + '. Please report this error to the administrator.');
                return true;
              }
              if ( ! this.summon_show[summ.d]) {
                return false;
              }
              return this.data_model[e.key].show(summ[e.key]);
            });

            if (show && this.showSummons) {
              this.getFilters.forEach(e => {
                if (summ[e.key] !== undefined) {
                  this.data_model[e.key].add(summ[e.key], newDataCount[e.key]);
                }                
              });
            }
            return show;
          })
        );
        this.$set(this.data_count, 1, newDataCount);
        return summons;
      }
      return [[],[],[],[],[],[],[]];
    },
    getUnits() {
      return [
        this.getCharacters,
        this.getSummons
      ];
    }
